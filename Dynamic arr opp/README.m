# Dynamic Array Operations

## Objective
To perform operations on a dynamic array using malloc().

## Problem Statement
Implement insertion, deletion, updating, searching, displaying, sorting, and compare Linear Search and Binary Search.

## Algorithm
1. Allocate memory using malloc().
2. Perform array operations using the menu.
3. Sort the array before Binary Search.
4. Free memory before exiting.

## Time Complexity
Insert: O(1)
Delete: O(n)
Update: O(1)
Display: O(n)
Linear Search: O(n)
Binary Search: O(log n)
Sort: O(n²)

## Space Complexity
O(n)

## Sample Input
10
5 10 15 20

## Sample Output
Element Inserted.
Array Sorted.
Found at Position 3

## Learning Outcome
Learned dynamic memory allocation, searching, sorting, and array operations.